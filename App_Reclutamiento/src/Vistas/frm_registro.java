    private void fnt_consultarDpersona(int id){
        posicionInt = 0;
        swBln = false;
        for(int i = 0; i < datosPersonales.size(); i++){
                if(id == datosPersonales.get(i).getIdint()){
                    posicionInt = i;
                    swBln = true;
                    break;
                }
        }
        if(swBln == false){
                JOptionPane.showMessageDialog(null, "No se encontro el candidato",
                "Consultar",JOptionPane.ERROR_MESSAGE);
        }else{
                txt_nombre.setText(datosPersonales.get(posicionInt).getNombreStr());
                txt_contacto.setText(datosPersonales.get(posicionInt).getContactoStr());
                txt_direccion.setText(datosPersonales.get(posicionInt).getDireccionStr());
                txt_correo.setText(datosPersonales.get(posicionInt).getCorreoStr());
                txt_perfil.setText(datosPersonales.get(posicionInt).getPerfilStr());
        }
    
    }

